# Definimos una lista que contiene las asignaturas del curso
asignaturas = ["Matemáticas", "Física", "Química", "Historia", "Lengua"]

# Recorremos cada asignatura en la lista usando un bucle for
print("Mis asignaturas:")
for asignatura in asignaturas:
    # Mostramos un mensaje personalizado para cada asignatura
    print(f"Yo estudio {asignatura}")
